-- 3-3. 都市別の投入件数（期待値: 3節本文コメントの区数と一致。北九州市・福岡市は既存7件のまま）
SELECT
  CASE
    WHEN name LIKE '札幌市%' THEN '札幌市' WHEN name LIKE '仙台市%' THEN '仙台市'
    WHEN name LIKE 'さいたま市%' THEN 'さいたま市' WHEN name LIKE '千葉市%' THEN '千葉市'
    WHEN name LIKE '横浜市%' THEN '横浜市' WHEN name LIKE '川崎市%' THEN '川崎市'
    WHEN name LIKE '相模原市%' THEN '相模原市' WHEN name LIKE '新潟市%' THEN '新潟市'
    WHEN name LIKE '静岡市%' THEN '静岡市' WHEN name LIKE '浜松市%' THEN '浜松市'
    WHEN name LIKE '名古屋市%' THEN '名古屋市' WHEN name LIKE '京都市%' THEN '京都市'
    WHEN name LIKE '大阪市%' THEN '大阪市' WHEN name LIKE '堺市%' THEN '堺市'
    WHEN name LIKE '神戸市%' THEN '神戸市' WHEN name LIKE '岡山市%' THEN '岡山市'
    WHEN name LIKE '広島市%' THEN '広島市' WHEN name LIKE '北九州市%' THEN '北九州市'
    WHEN name LIKE '福岡市%' THEN '福岡市' WHEN name LIKE '熊本市%' THEN '熊本市'
    ELSE '（政令市以外）'
  END AS city,
  COUNT(*) AS ward_count
FROM municipalities
GROUP BY 1
ORDER BY city;

-- 3-4. code重複確認（UNIQUE制約があるため理論上0件、念のため）
SELECT code, COUNT(*) FROM municipalities GROUP BY code HAVING COUNT(*) > 1;

